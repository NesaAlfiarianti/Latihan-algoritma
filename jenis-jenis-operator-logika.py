# Mendefinisikan dua variabel boolean
x = True
y = False

# Operator AND
print("x AND y:", x and y)  # Output: False (keduanya harus True untuk menghasilkan True)

# Operator OR
print("x OR y:", x or y)     # Output: True (minimal salah satu harus True)

# Operator NOT
print("NOT x:", not x)       # Output: False (membalik nilai x)
print("NOT y:", not y)       # Output: True (membalik nilai y)

