#Mendefinisikan string
nama = "nesa"
alamat = "mamasa"

# Menggabungkan string
info = nama + " di " + alamat
print("Informasi:", info)

# Mengakses karakter dalam string
huruf_pertama = nama[0]
print("Huruf Pertama dari Nama:", huruf_pertama)

# Menghitung panjang string
panjang_nama = len(nama)
print("Panjang Nama:", panjang_nama)

# Mengubah string menjadi kapital
nama_kapital = nama.upper()
print("Nama dalam Huruf Kapital:", nama_kapital)