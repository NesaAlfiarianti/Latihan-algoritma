 # Mendefinisikan dua bilangan
a = 5  # dalam biner: 0101
b = 3  # dalam biner: 0011

# Operator AND
print("AND (a & b):", a & b)  # Hasil: 1 (0001)

# Operator OR
print("OR (a | b):", a | b)   # Hasil: 7 (0111)

# Operator XOR
print("XOR (a ^ b):", a ^ b)  # Hasil: 6 (0110)

# Operator NOT
print("NOT (~a):", ~a)        # Hasil: -6 (negasi dari 5)

# Operator Shift Kiri
print("Shift Kiri (a << 1):", a << 1)  # Hasil: 10 (1010)

# Operator Shift Kanan
print("Shift Kanan (a >> 1):", a >> 1)  # Hasil: 2 (0010)
