# String
nama = "Nesa"
print("String:", nama)

# Integer
umur = 17
print("Integer:", umur)

# Float
tinggi = 163
print("Float:", tinggi)

# Complex Number
bilangan_kompleks = 2 + 3j
print("Complex Number:", bilangan_kompleks)

# Boolean
is_active = True
print("Boolean:", is_active)

# List
angka = [1, 2, 3, 4, 5]
print("List:", angka)

# Tuple
koordinat = (10.0, 20.0)
print("Tuple:", koordinat)

# Set
angka_unik = {1, 2, 3, 4, 5}
print("Set:", angka_unik)

# Dictionary
data_siswa = {'nama': 'nesa', 'umur': 17}
print("Dictionary:", data_siswa)

