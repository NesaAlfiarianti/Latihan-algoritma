# Mendefinisikan dua variabel
a = 10
b = 3

# Penjumlahan
jumlah = a + b
print("Penjumlahan:", jumlah)  # Output: 13

# Pengurangan
selisih = a - b
print("Pengurangan:", selisih)  # Output: 7

# Perkalian
kali = a * b
print("Perkalian:", kali)  # Output: 30

# Pembagian
bagi = a / b
print("Pembagian:", bagi)  # Output: 3.3333...

# Pembagian Bulat
bagi_bulat = a // b
print("Pembagian Bulat:", bagi_bulat)  # Output: 3

# Modulus
modulus = a % b
print("Modulus:", modulus)  # Output: 1

# Pangkat
pangkat = a ** b
print("Pangkat:", pangkat)  # Output: 1000