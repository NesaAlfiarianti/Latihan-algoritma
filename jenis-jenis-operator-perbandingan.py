# Mendefinisikan dua angka
x = 8
y = 5

# Sama dengan
print("x sama dengan y:", x == y)  # Output: False

# Tidak sama dengan
print("x tidak sama dengan y:", x != y)  # Output: True

# Lebih besar dari
print("x lebih besar dari y:", x > y)  # Output: True

# Kurang dari
print("x kurang dari y:", x < y)  # Output: False

# Lebih besar atau sama dengan
print("x lebih besar atau sama dengan y:", x >= y)  # Output: True

# Kurang dari atau sama dengan
print("x kurang dari atau sama dengan y:", x <= y)  # Output: False