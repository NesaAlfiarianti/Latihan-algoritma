# Mendefinisikan variabel
x = 10
print("Nilai awal x:", x)

# Addition Assignment
x += 5  # x = x + 5
print("Setelah x += 5:", x)  # Hasil: 15

# Subtraction Assignment
x -= 3  # x = x - 3
print("Setelah x -= 3:", x)  # Hasil: 12

# Multiplication Assignment
x *= 2  # x = x * 2
print("Setelah x *= 2:", x)  # Hasil: 24

# Division Assignment
x /= 4  # x = x / 4
print("Setelah x /= 4:", x)  # Hasil: 6.0