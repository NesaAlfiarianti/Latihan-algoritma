# Operator Aritmatika
a = 10
b = 5

# Penjumlahan
jumlah = a + b
print("Jumlah:", jumlah)  # Output: 15

# Pengurangan
selisih = a - b
print("Selisih:", selisih)  # Output: 5

# Perbandingan
is_sama = (a == b)
print("Apakah a sama dengan b?", is_sama)  # Output: False

# Operator Logika
x = True
y = False
print("x dan y:", x and y)  # Output: False

# Operator Penugasan
c = 7
c += 3  # c = c + 3
print("Nilai c setelah penugasan:", c)  # Output: 10

# Operator Keanggotaan
daftar = [1, 2, 3, 4, 5]
print("Apakah 3 ada dalam daftar?", 3 in daftar)  # Output: True